package Arrays;

public class Tagged_Array {
	public void watch() {
		int [][] g = {{1,2,3,5},{1,2,3},{1,2},{1}};
		System.out.println("-----------------------------------TAGGED-ARRAY--------------------------------------------------------------------------------");

		for(int [] q:g) {
			for(int e:q) {
				System.out.print(e+ " ");
				
				
			}
			System.out.println();

		}
	}

}
