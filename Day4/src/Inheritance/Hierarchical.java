package Inheritance;

public class Hierarchical {
	public void Main() {
		System.out.println("..............WEL COME TO HIERARCHICAL INHERITANCE................");
	}

}
class H extends Hierarchical{
	public void name() {
		System.out.println("MY SELF SRINU CAUHAN");
	}
}
class I extends Hierarchical{
	public void age() {
		System.out.println("Age:21");
	}
}
class E extends Hierarchical{
	public void height() {
		System.out.println("Height:172CM");
	}
}
